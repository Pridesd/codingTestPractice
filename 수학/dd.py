ls = [[1, 9], [2, 8], [3, 7], [4, 6], [5, 5], [6, 4], [7, 3], [8, 2], [9, 1]]

# 람다를 사용하지 않았을 때


def keySort(nList):
    return nList[1]


ls.sort(key=keySort)
print(ls)
# 출력: [[9, 1], [8, 2], [7, 3], [6, 4], [5, 5], [4, 6], [3, 7], [2, 8], [1, 9]]
